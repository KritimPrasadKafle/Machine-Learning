def print_asterisk_pattern():
    """Prints a 3x5 rectangle of asterisks using nested loops"""
    for row in range(3):  # Outer loop for rows
        for col in range(5):  # Inner loop for columns
            print("* - star.py:5", end="")  # Print asterisk without newline
        print()  # Print newline after each row

